namespace NS_OpenGLWindowPCGame
{



//==================================================================================================================================
// クラス定義
//==================================================================================================================================

//----------------------------------------------------------------------------------------------------------------------------------
// @purpose : ゲームオブジェクト削除クラス
//
// @note : 
//
// ゲームオブジェクトクラスをリストから削除する為のクラス。
//----------------------------------------------------------------------------------------------------------------------------------
class GameObjectRemove 
{
public:

	// ()演算子オーバーロード
	bool operator () ( GameObject* pGameObject )
	{
		if ( pGameObject->IsDestroy() )
		{
			SAFE_DELETE( pGameObject );

			return true;
		}
		


		return false;
	}
};



}	// namespace NS_OpenGLWindowPCGame



