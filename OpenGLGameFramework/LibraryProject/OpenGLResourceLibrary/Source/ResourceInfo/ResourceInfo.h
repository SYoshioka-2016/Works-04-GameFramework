namespace NS_Resource
{

	

//==================================================================================================================================
// 構造体定義
//==================================================================================================================================

//----------------------------------------------------------------------------------------------------------------------------------
// @purpose : リソース情報構造体
//
// @note : 
//
// リソース関連の情報をまとめた構造体。
//----------------------------------------------------------------------------------------------------------------------------------
struct RESOURCE_INFO
{
	// ID
	unsigned int	id;

	// リソースファイルのパス
//	const char*		path;
	const std::string		path;

	// リソースタイプ
	eRESOURCE_TYPE	resourceType;



	// コンストラクタ
	RESOURCE_INFO() :
		RESOURCE_INFO( 0, "", eRESOURCE_TYPE::UNKNOWN )
	{ }

	// コンストラクタ
	RESOURCE_INFO(
		const unsigned int	uiResourceID,
		const char*			strPath,
		eRESOURCE_TYPE		resType
	) :
		id( uiResourceID ),
		path( strPath ),
		resourceType( resType )
	{ }



	// 比較演算子オーバーロード
	bool operator < ( const RESOURCE_INFO& resourceInfo ) const
	{
		return ( id < resourceInfo.id );
    }
};



}	// namespace NS_Resource



